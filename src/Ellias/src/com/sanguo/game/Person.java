package com.sanguo.game;

public class Person {
    private int id;
    private String name;
    private int wuli;
    private int zhili;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWuli() {
        return this.wuli;
    }

    public void setWuli(int wuli) {
        this.wuli = wuli;
    }

    public int getZhili() {
        return this.zhili;
    }

    public void setZhili(int zhili) {
        this.zhili = zhili;
    }
}